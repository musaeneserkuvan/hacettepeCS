

public class Nodes {

    private Buttons buttonsObj;

    private Texts textObj;

    private ComboBoxs comboboxObj;

    private Textfields textfieldObj;

    private Tableviews tableviewObj;


    public Nodes(){}



    public Buttons getButtonsObj() {
        return buttonsObj;
    }

    public Texts getTextObj() {
        return textObj;
    }

    public ComboBoxs getComboboxObj() {
        return comboboxObj;
    }

    public Textfields getTextfieldObj() {
        return textfieldObj;
    }

    public Tableviews getTableviewObj() {
        return tableviewObj;
    }

    public void setButtonsObj(Buttons buttonsObj) {
        this.buttonsObj = buttonsObj;
    }

    public void setTextObj(Texts textObj) {
        this.textObj = textObj;
    }

    public void setComboboxObj(ComboBoxs comboboxObj) {
        this.comboboxObj = comboboxObj;
    }

    public void setTextfieldObj(Textfields textfieldObj) {
        this.textfieldObj = textfieldObj;
    }

    public void setTableviewObj(Tableviews tableviewObj) {
        this.tableviewObj = tableviewObj;
    }
}
